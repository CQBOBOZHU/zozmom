package com.zozmom.model;

public class ShareUrlModel {
	String paySuccessUrl;//支付完成 分享url
	String detailUrl;//分享详情 url
	String productUrl;//分享商品 url
	
	
	public String getPaySuccessUrl() {
		return paySuccessUrl;
	}
	public void setPaySuccessUrl(String paySuccessUrl) {
		this.paySuccessUrl = paySuccessUrl;
	}
	public String getDetailUrl() {
		return detailUrl;
	}
	public void setDetailUrl(String detailUrl) {
		this.detailUrl = detailUrl;
	}
	public String getProductUrl() {
		return productUrl;
	}
	public void setProductUrl(String productUrl) {
		this.productUrl = productUrl;
	}
	
	
}
